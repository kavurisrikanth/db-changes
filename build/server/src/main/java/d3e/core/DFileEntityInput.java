package d3e.core;

public class DFileEntityInput {
    private String id;
    private String name;
    private long size;

    public String _type() {
        return "DFile";
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
