import 'dart:math' as m;

class Math {
  static final double e = m.e;
  static final double ln2 = m.ln2;
  static final double ln10 = m.ln10;
  static final double log2e = m.log2e;
  static final double log10e = m.log2e;
  static final double pi = m.pi;
  static final double sqrt1_2 = m.sqrt1_2;
  static final double sqrt2 = m.sqrt2;
  static double sin(double val) {
    return m.sin(val);
  }

  static double cos(double val) {
    return m.cos(val);
  }

  static double pow(double val1, double val2) {
    return m.pow(val1, val2);
  }

  static double acos(double val) {
    return m.acos(val);
  }

  static double asin(double val) {
    return m.asin(val);
  }

  static double atan(double val) {
    return m.atan(val);
  }

  static double atan2(double a, double b) {
    return m.atan2(a, b);
  }

  static double exp(double val) {
    return m.exp(val);
  }

  static double log(double val) {
    return m.log(val);
  }

  static int maxInt(int a, int b) {
    return m.max(a, b);
  }

  static double max(double a, double b) {
    return m.max(a, b);
  }

  static int minInt(int a, int b) {
    return m.min(a, b);
  }

  static double min(double a, double b) {
    return m.min(a, b);
  }

  static double sqrt(double val) {
    return m.sqrt(val);
  }

  static double tan(double val) {
    return m.tan(val);
  }
}
